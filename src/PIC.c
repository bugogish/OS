#include <ioport.h>
#include <desc.h>

void PIC_setup(){
	out8(0x20, 0x11);/*инициализация*/
	out8(0xA0, 0x11); 
	out8(0x21, 0x20);/*32 запись для мастера*/
	out8(0xA1, 0x28);/*40 для слэйва*/
	out8(0x21, 0x04);/*Slave на втором входе*/
	out8(0xA1, 0x02);
	out8(0x21, 0x01);
	out8(0xA1, 0x01);
        out8(0x21, 0xFF);
	out8(0xA1, 0xFF);
}
